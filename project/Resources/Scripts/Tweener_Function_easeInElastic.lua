function Tweener.easeInElastic ( t, b, c, d, a, p )
--------------------------------------------------------------------------------
	
	if (t==0)   then return b;      end
    t = t/d
    if (t==1)   then return b+c;    end
    if (p==nil) then p=d*.3         end
    local s;
    if (a==nil or a < math.abs(c)) then a=c; s=p/4;
    else s = p/(2*3.14159265) * math.asin (c/a); end
    t=t-1
    return -(a*math.pow(2,10*t) * math.sin( (t*d-s)*(2*3.14159265)/p )) + b;
	
--------------------------------------------------------------------------------
end
