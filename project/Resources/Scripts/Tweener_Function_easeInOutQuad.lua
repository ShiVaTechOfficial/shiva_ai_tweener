function Tweener.easeInOutQuad ( t, b, c, d )
--------------------------------------------------------------------------------
	
    t = t / d / 2
	if ( t < 1) then return c/2*t*t + b end
    t = t - 1
    return -c/2 * ( t * ( t-2 ) - 1) + b;
	
--------------------------------------------------------------------------------
end
