function Tweener.easeInOutCirc ( t, b, c, d )
--------------------------------------------------------------------------------
	
    t = t/d/2
	if (t < 1) then return -c/2 * (math.sqrt(1 - t*t) - 1) + b; end
    t=t-2
    return c/2 * (math.sqrt(1 - (t)*t) + 1) + b;
	
--------------------------------------------------------------------------------
end
