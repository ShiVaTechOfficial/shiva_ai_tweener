function Tweener.easeOutBounce ( t, b, c, d )
--------------------------------------------------------------------------------
	
    t = t/d
	if (t < (1/2.75)) then
        return c*(7.5625*t*t) + b;
    elseif (t < (2/2.75)) then
        t = t - (1.5/2.75)
        return c*(7.5625*t*t + .75) + b;
    elseif (t < (2.5/2.75)) then
        t = t - (2.25/2.75)
        return c*(7.5625*t*t + .9375) + b;
    else 
        t = t - (2.625/2.75)
        return c*(7.5625*t*t + .984375) + b;
    end
	
--------------------------------------------------------------------------------
end
