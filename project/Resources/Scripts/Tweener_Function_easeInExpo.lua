function Tweener.easeInExpo ( t, b, c, d )
--------------------------------------------------------------------------------
	
    if ( t==0 ) then return b
    else
	return c * math.pow(2, 10 * (t/d - 1)) + b - c * 0.001; end
	
--------------------------------------------------------------------------------
end
