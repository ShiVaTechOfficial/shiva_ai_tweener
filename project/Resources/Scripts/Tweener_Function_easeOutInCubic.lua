function Tweener.easeOutInCubic ( t, b, c, d )
--------------------------------------------------------------------------------
	
	if (t < d/2) then return this.easeOutCubic (t*2, b, c/2, d); end
    return this.easeInCubic((t*2)-d, b+c/2, c/2, d);
	
--------------------------------------------------------------------------------
end
