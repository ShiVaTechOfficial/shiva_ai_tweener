function Tweener.easeOutInElastic ( t, b, c, d, a, p )
--------------------------------------------------------------------------------
	
	if (t < d/2) then return this.easeOutElastic (t*2, b, c/2, d, a, p); end
    return this.easeInElastic((t*2)-d, b+c/2, c/2, d, a, p);
	
--------------------------------------------------------------------------------
end
