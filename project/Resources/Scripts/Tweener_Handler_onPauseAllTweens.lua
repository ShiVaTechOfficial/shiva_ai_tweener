function Tweener.onPauseAllTweens (  )
--------------------------------------------------------------------------------
	
    local paused = this.tPaused ( )
    local nTweenSize = table.getSize ( paused )
	for i = 0, nTweenSize do
        table.setAt ( paused, i, true )
    end
	
--------------------------------------------------------------------------------
end
