function Tweener.easeInOutExpo ( t, b, c, d )
--------------------------------------------------------------------------------
	
	if (t==0) then return b; end 
    if (t==d) then return b+c; end
    t = t/d/2
    if (t < 1) then return c/2 * math.pow(2, 10 * (t - 1)) + b - c * 0.0005; end
    t = t-1
    return c/2 * 1.0005 * (-math.pow(2, -10 * t) + 2) + b;
	
--------------------------------------------------------------------------------
end
