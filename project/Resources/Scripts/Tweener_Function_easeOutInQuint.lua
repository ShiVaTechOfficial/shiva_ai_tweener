function Tweener.easeOutInQuint ( t, b, c, d )
--------------------------------------------------------------------------------
	
	if (t < d/2) then return this.easeOutQuint (t*2, b, c/2, d); end
    return this.easeInQuint((t*2)-d, b+c/2, c/2, d);
	
--------------------------------------------------------------------------------
end
