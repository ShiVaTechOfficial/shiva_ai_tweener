function Tweener.easeOutInBack ( t, b, c, d, s )
--------------------------------------------------------------------------------
	
	if (t < d/2) then return this.easeOutBack (t*2, b, c/2, d, s); end 
    return this.easeInBack((t*2)-d, b+c/2, c/2, d, s);
	
--------------------------------------------------------------------------------
end
