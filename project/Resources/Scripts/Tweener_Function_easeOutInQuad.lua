function Tweener.easeOutInQuad ( t, b, c, d )
--------------------------------------------------------------------------------
	
	if (t < d/2) then return this.easeOutQuad (t*2, b, c/2, d) end
	return this.easeInQuad((t*2)-d, b+c/2, c/2, d);
	
--------------------------------------------------------------------------------
end
