function Tweener.easeInOutBack ( t, b, c, d, s )
--------------------------------------------------------------------------------
	
	if (s==nil) then s = 1.70158; end
    t=t/d/2
    if (t < 1) then
        s = s * 1.525
        return c/2*(t*t*((s+1)*t - s)) + b;
    end
    t=t-2
    s = s * 1.525
    return c/2*(t*t*((s+1)*t + s) + 2) + b;
	
--------------------------------------------------------------------------------
end
