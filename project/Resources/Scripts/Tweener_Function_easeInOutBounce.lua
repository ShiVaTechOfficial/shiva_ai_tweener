function Tweener.easeInOutBounce ( t, b, c, d )
--------------------------------------------------------------------------------
	
	if (t < d/2) then return this.easeInBounce (t*2, 0, c, d) * .5 + b;
    else return this.easeOutBounce (t*2-d, 0, c, d) * .5 + c*.5 + b; end
	
--------------------------------------------------------------------------------
end
